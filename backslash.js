let message = 'The boy\'s name is muhammad'; //escaping quotes
console.log("Message: ", message);

message = "The boy's name is: \nMuhammad";
console.log("Message: ", message);

message = "The boy's name is: \b\b\b\bMuhammad";
console.log("Message: ", message);

message = "The boy's name is: \tMuhammad";
console.log("Message: ", message);
